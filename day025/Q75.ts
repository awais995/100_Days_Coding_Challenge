// Compound Assignment Operators

function compoundoperators () {
    
    let x = 4;  //variable declare
    console.log(x);
    x += 2;     // add 2 into x
    console.log(x); 
    x -= 2;     // subtract 2 from x
    console.log(x);
    x *= 2;     // multiply 2 with x
    console.log(x);
    x /= 2;     // divide 2 with x
    console.log(x);
}

compoundoperators();